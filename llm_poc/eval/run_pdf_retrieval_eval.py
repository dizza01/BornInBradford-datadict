"""Run retrieval evaluation for PDF triples against ChromaDB.

Evaluates whether each triple's `source_chunk_id` is retrieved for its question,
using top-k cutoffs (default: 1, 3, 5, 10).

Expected input triples are generated by:
    eval/generate_pdf_retrieval_triples.py

Usage
-----

    ../../.venv/bin/python eval/run_pdf_retrieval_eval.py

    ../../.venv/bin/python eval/run_pdf_retrieval_eval.py \
        --triples eval/pdf_retrieval_triples.jsonl \
        --top-k 1 3 5 10 \
        --output eval/pdf_retrieval_eval_results.json
"""

from __future__ import annotations

import argparse
from collections import Counter, defaultdict
import csv
from datetime import datetime
import json
import math
import re
import statistics
import sys
from pathlib import Path
from typing import Any

ROOT_DIR = Path(__file__).resolve().parents[1]
if str(ROOT_DIR) not in sys.path:
    sys.path.insert(0, str(ROOT_DIR))

from bib_research_assistant import get_chroma_client

DEFAULT_TRIPLES = Path(__file__).resolve().parent / "pdf_retrieval_triples.jsonl"
DEFAULT_OUTPUT = Path(__file__).resolve().parent / "pdf_retrieval_eval_results.json"
DEFAULT_RESULTS_DIR = Path(__file__).resolve().parent / "results"
DEFAULT_TOP_K = [1, 3, 5, 10]
DEFAULT_MODES = ["dense", "hybrid", "hybrid_rerank"]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evaluate retrieval of source PDF chunks using generated triples.",
    )
    parser.add_argument(
        "--triples",
        type=Path,
        default=DEFAULT_TRIPLES,
        help="Path to JSONL triples file generated by generate_pdf_retrieval_triples.py",
    )
    parser.add_argument(
        "--top-k",
        type=int,
        nargs="+",
        default=DEFAULT_TOP_K,
        help="Top-k cutoffs to evaluate (default: 1 3 5 10)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help="Path to write evaluation results JSON",
    )
    parser.add_argument(
        "--max-queries",
        type=int,
        default=0,
        help="Optional cap on number of triples to evaluate (0 = all).",
    )
    parser.add_argument(
        "--modes",
        type=str,
        nargs="+",
        default=DEFAULT_MODES,
        help="Retrieval modes to benchmark (dense, hybrid, hybrid_rerank).",
    )
    parser.add_argument(
        "--dense-pool",
        type=int,
        default=60,
        help="Dense retrieval candidate pool size for hybrid/rerank modes.",
    )
    parser.add_argument(
        "--sparse-pool",
        type=int,
        default=60,
        help="Sparse (BM25) retrieval candidate pool size for hybrid/rerank modes.",
    )
    parser.add_argument(
        "--rerank-pool",
        type=int,
        default=50,
        help="Number of fused candidates reranked for hybrid_rerank mode.",
    )
    parser.add_argument(
        "--rrf-k",
        type=int,
        default=60,
        help="RRF constant used for dense+sparse rank fusion in hybrid modes.",
    )
    parser.add_argument(
        "--run-name",
        type=str,
        default="",
        help="Optional run label. When provided, results are auto-saved to eval/results/<run-name>_<timestamp>.json and appended to eval/results/comparison.csv",
    )
    return parser.parse_args()


def _load_triples(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        raise FileNotFoundError(f"Triples file not found: {path}")

    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as fh:
        for line_no, line in enumerate(fh, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                rec = json.loads(line)
            except json.JSONDecodeError as exc:
                raise ValueError(f"Invalid JSON at line {line_no}: {exc}") from exc

            if not rec.get("question") or not rec.get("source_chunk_id"):
                continue
            rows.append(rec)
    return rows


def _safe_median(values: list[int]) -> float | None:
    if not values:
        return None
    return float(statistics.median(values))


def _safe_run_name(run_name: str) -> str:
    safe = re.sub(r"[^a-zA-Z0-9._-]+", "_", run_name.strip())
    return safe.strip("_") or "run"


def _append_comparison_csv(
    csv_path: Path,
    run_name: str,
    output_path: Path,
    eval_results: dict[str, Any],
) -> None:
    csv_path.parent.mkdir(parents=True, exist_ok=True)
    file_exists = csv_path.exists()

    fieldnames = [
        "timestamp",
        "run_name",
        "mode",
        "n_queries",
        "recall_at_1",
        "recall_at_3",
        "recall_at_5",
        "recall_at_10",
        "mrr",
        "avg_hit_rank",
        "median_hit_rank",
        "top_k",
        "dense_pool",
        "sparse_pool",
        "rerank_pool",
        "rrf_k",
        "output_file",
    ]

    config = eval_results.get("config", {})
    now = datetime.now().isoformat(timespec="seconds")

    with csv_path.open("a", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        if not file_exists:
            writer.writeheader()

        for mode, payload in eval_results.get("modes", {}).items():
            summary = payload.get("summary", {})
            recall = summary.get("recall_at_k", {})
            writer.writerow(
                {
                    "timestamp": now,
                    "run_name": run_name,
                    "mode": mode,
                    "n_queries": summary.get("n_queries", ""),
                    "recall_at_1": recall.get("1", ""),
                    "recall_at_3": recall.get("3", ""),
                    "recall_at_5": recall.get("5", ""),
                    "recall_at_10": recall.get("10", ""),
                    "mrr": summary.get("mrr", ""),
                    "avg_hit_rank": summary.get("avg_hit_rank", ""),
                    "median_hit_rank": summary.get("median_hit_rank", ""),
                    "top_k": " ".join(str(k) for k in config.get("top_k", [])),
                    "dense_pool": config.get("dense_pool", ""),
                    "sparse_pool": config.get("sparse_pool", ""),
                    "rerank_pool": config.get("rerank_pool", ""),
                    "rrf_k": config.get("rrf_k", ""),
                    "output_file": str(output_path),
                }
            )


def _tokenize(text: str) -> list[str]:
    return re.findall(r"[a-z0-9]+", (text or "").lower())


def _rrf_fuse(rank_lists: list[list[str]], rrf_k: int) -> list[str]:
    scores: dict[str, float] = defaultdict(float)
    for ranked_ids in rank_lists:
        for rank, doc_id in enumerate(ranked_ids, start=1):
            scores[doc_id] += 1.0 / (rrf_k + rank)
    return [doc_id for doc_id, _ in sorted(scores.items(), key=lambda kv: kv[1], reverse=True)]


class SparseBM25:
    def __init__(self, ids: list[str], docs: list[str], k1: float = 1.2, b: float = 0.75):
        self.ids = ids
        self.k1 = k1
        self.b = b
        self.n_docs = len(ids)
        self.doc_len: list[int] = []
        self.avgdl = 0.0
        self.postings: dict[str, list[tuple[int, int]]] = defaultdict(list)
        self.df: dict[str, int] = {}
        self.idf: dict[str, float] = {}
        self._build(docs)

    def _build(self, docs: list[str]) -> None:
        total_len = 0
        for idx, text in enumerate(docs):
            tokens = _tokenize(text)
            total_len += len(tokens)
            self.doc_len.append(len(tokens))

            counts = Counter(tokens)
            for term, tf in counts.items():
                self.postings[term].append((idx, tf))

        self.avgdl = (total_len / self.n_docs) if self.n_docs else 0.0

        for term, plist in self.postings.items():
            df = len(plist)
            self.df[term] = df
            # Standard BM25 IDF variant
            self.idf[term] = math.log(1.0 + ((self.n_docs - df + 0.5) / (df + 0.5)))

    def search(self, query: str, top_n: int) -> list[str]:
        if top_n <= 0 or not query:
            return []
        if self.n_docs == 0:
            return []

        scores: dict[int, float] = defaultdict(float)
        for term in _tokenize(query):
            plist = self.postings.get(term)
            if not plist:
                continue
            idf = self.idf.get(term, 0.0)
            for doc_idx, tf in plist:
                dl = self.doc_len[doc_idx] if doc_idx < len(self.doc_len) else 0
                denom = tf + self.k1 * (1.0 - self.b + self.b * (dl / (self.avgdl or 1.0)))
                scores[doc_idx] += idf * ((tf * (self.k1 + 1.0)) / (denom or 1.0))

        ranked = sorted(scores.items(), key=lambda kv: kv[1], reverse=True)[:top_n]
        return [self.ids[idx] for idx, _ in ranked]


def _simple_rerank_score(query: str, doc: str) -> float:
    q_tokens = _tokenize(query)
    d_tokens = _tokenize(doc)
    if not q_tokens or not d_tokens:
        return 0.0

    q_set = set(q_tokens)
    d_set = set(d_tokens)
    overlap = len(q_set & d_set) / max(1, len(q_set))

    q_bigrams = set(zip(q_tokens, q_tokens[1:]))
    d_bigrams = set(zip(d_tokens, d_tokens[1:]))
    bigram_overlap = len(q_bigrams & d_bigrams) / max(1, len(q_bigrams)) if q_bigrams else 0.0

    contains_query = 1.0 if query.lower().strip() in doc.lower() else 0.0

    return (0.65 * overlap) + (0.25 * bigram_overlap) + (0.10 * contains_query)


def _build_summary(per_query: list[dict[str, Any]], top_k: list[int]) -> dict[str, Any]:
    n = len(per_query)
    first_hit_ranks = [q["hit_rank"] for q in per_query if q["hit_rank"]]

    recall_at_k = {
        str(k): (sum(1 for q in per_query if q[f"hit_at_{k}"]) / n if n else 0.0)
        for k in top_k
    }
    mrr = (sum(1.0 / q["hit_rank"] for q in per_query if q["hit_rank"]) / n) if n else 0.0

    return {
        "n_queries": n,
        "top_k": top_k,
        "recall_at_k": recall_at_k,
        "mrr": mrr,
        "avg_hit_rank": (sum(first_hit_ranks) / len(first_hit_ranks)) if first_hit_ranks else None,
        "median_hit_rank": _safe_median(first_hit_ranks),
    }


def evaluate_retrieval_side_by_side(
    triples: list[dict[str, Any]],
    top_k: list[int],
    modes: list[str],
    dense_pool: int,
    sparse_pool: int,
    rerank_pool: int,
    rrf_k: int,
) -> dict[str, Any]:
    top_k = sorted({k for k in top_k if k > 0})
    if not top_k:
        raise ValueError("top_k must contain at least one positive integer")

    allowed_modes = {"dense", "hybrid", "hybrid_rerank"}
    requested_modes = []
    seen = set()
    for mode in modes:
        m = mode.strip().lower()
        if m in allowed_modes and m not in seen:
            requested_modes.append(m)
            seen.add(m)
    if not requested_modes:
        raise ValueError("At least one valid mode is required: dense, hybrid, hybrid_rerank")

    max_k = max(top_k)
    dense_pool = max(dense_pool, max_k)
    sparse_pool = max(sparse_pool, max_k)
    rerank_pool = max(rerank_pool, max_k)

    client = get_chroma_client()
    collection = client.get_collection("bib_papers")

    # Build sparse corpus only when hybrid modes are requested.
    sparse_index: SparseBM25 | None = None
    doc_by_id: dict[str, str] = {}
    if any(m in requested_modes for m in ("hybrid", "hybrid_rerank")):
        sparse_source = collection.get(where={"source": "pdf_fulltext"}, include=["documents"])
        sparse_ids = sparse_source.get("ids", []) or []
        sparse_docs = sparse_source.get("documents", []) or []
        doc_by_id = {doc_id: doc for doc_id, doc in zip(sparse_ids, sparse_docs)}
        sparse_index = SparseBM25(sparse_ids, sparse_docs)

    by_mode_per_query: dict[str, list[dict[str, Any]]] = {m: [] for m in requested_modes}

    for idx, triple in enumerate(triples, start=1):
        question = str(triple.get("question", "")).strip()
        source_chunk_id = str(triple.get("source_chunk_id", "")).strip()
        query_id = str(triple.get("query_id", f"q_{idx}"))
        question_type = triple.get("question_type", "")

        if not question or not source_chunk_id:
            continue

        dense_result = collection.query(
            query_texts=[question],
            n_results=max(dense_pool, max_k),
            where={"source": "pdf_fulltext"},
            include=[],
        )
        dense_ids = dense_result.get("ids", [[]])[0] or []

        sparse_ids = sparse_index.search(question, sparse_pool) if sparse_index else []
        fused_ids = _rrf_fuse([dense_ids[:dense_pool], sparse_ids[:sparse_pool]], rrf_k=rrf_k) if sparse_ids else dense_ids

        mode_to_retrieved: dict[str, list[str]] = {
            "dense": dense_ids[:max_k],
            "hybrid": fused_ids[:max_k],
        }

        if "hybrid_rerank" in requested_modes:
            rerank_candidates = fused_ids[:rerank_pool]
            reranked = sorted(
                rerank_candidates,
                key=lambda doc_id: _simple_rerank_score(question, doc_by_id.get(doc_id, "")),
                reverse=True,
            )
            mode_to_retrieved["hybrid_rerank"] = reranked[:max_k]

        for mode in requested_modes:
            retrieved_ids = mode_to_retrieved[mode]
            hit_rank: int | None = None
            for rank, chunk_id in enumerate(retrieved_ids, start=1):
                if chunk_id == source_chunk_id:
                    hit_rank = rank
                    break

            query_result = {
                "query_id": query_id,
                "question": question,
                "source_chunk_id": source_chunk_id,
                "question_type": question_type,
                "hit_rank": hit_rank,
                "retrieved_ids": retrieved_ids,
            }
            for k in top_k:
                query_result[f"hit_at_{k}"] = bool(hit_rank is not None and hit_rank <= k)

            by_mode_per_query[mode].append(query_result)

    by_mode = {
        mode: {
            "summary": _build_summary(by_mode_per_query[mode], top_k),
            "per_query": by_mode_per_query[mode],
        }
        for mode in requested_modes
    }

    return {
        "config": {
            "top_k": top_k,
            "modes": requested_modes,
            "dense_pool": dense_pool,
            "sparse_pool": sparse_pool,
            "rerank_pool": rerank_pool,
            "rrf_k": rrf_k,
        },
        "modes": by_mode,
    }


def main() -> None:
    args = parse_args()

    triples = _load_triples(args.triples)
    if not triples:
        print(f"❌ No valid triples found in: {args.triples}")
        sys.exit(1)

    if args.max_queries and args.max_queries > 0:
        triples = triples[: args.max_queries]

    print(f"📥 Loaded triples: {len(triples)}")
    print(f"🎯 Evaluating top-k: {args.top_k}")
    print(f"⚙️  Modes: {args.modes}")

    eval_results = evaluate_retrieval_side_by_side(
        triples=triples,
        top_k=args.top_k,
        modes=args.modes,
        dense_pool=args.dense_pool,
        sparse_pool=args.sparse_pool,
        rerank_pool=args.rerank_pool,
        rrf_k=args.rrf_k,
    )

    output_path = args.output
    if args.run_name.strip():
        safe_name = _safe_run_name(args.run_name)
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = DEFAULT_RESULTS_DIR / f"{safe_name}_{ts}.json"

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", encoding="utf-8") as fh:
        json.dump(eval_results, fh, indent=2, ensure_ascii=False)

    if args.run_name.strip():
        csv_path = DEFAULT_RESULTS_DIR / "comparison.csv"
        _append_comparison_csv(
            csv_path=csv_path,
            run_name=_safe_run_name(args.run_name),
            output_path=output_path,
            eval_results=eval_results,
        )

    print("\n════════════════════════════════════════════════════")
    for mode, payload in eval_results["modes"].items():
        summary = payload["summary"]
        print(f"\nMode: {mode}")
        print(f"Queries evaluated: {summary['n_queries']}")
        print(
            "Recall@k: "
            + ", ".join(
                f"R@{k}={summary['recall_at_k'][str(k)]:.3f}" for k in summary["top_k"]
            )
        )
        print(f"MRR: {summary['mrr']:.3f}")
        if summary["avg_hit_rank"] is not None:
            print(f"Avg hit rank: {summary['avg_hit_rank']:.2f}")
            print(f"Median hit rank: {summary['median_hit_rank']:.2f}")
        else:
            print("Avg/median hit rank: n/a (no hits)")
    print(f"Results saved: {output_path}")
    if args.run_name.strip():
        print(f"Comparison CSV updated: {DEFAULT_RESULTS_DIR / 'comparison.csv'}")


if __name__ == "__main__":
    main()
